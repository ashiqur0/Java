package Java_Tutorial.Java_DataTypes;

public class A {
    public static void main(String[] args) {
        boolean isStudent = true;   //DataTyple/Primitive/Boolean/boolean
        char c = 'A';               //DataTyple/Primitive/Numeric/Character/char
        byte a = 10;                //DataTyple/Primitive/Numeric/Integral/Integer/byte
        short b = 20;               //DataTyple/Primitive/Numeric/Integral/Integer/short
        int i = 30;                 //DataTyple/Primitive/Numeric/Integral/Integer/int
        long l = 20;                //DataTyple/Primitive/Numeric/Integral/Integer/long
        float d = 10.3f;            //DataTyple/Primitive/Numeric/Integral/Floating-Point/float
        double f = 100.4;           //DataTyple/Primitive/Numeric/Integral/Floating-Point/double
        String s = "abc";           //DataTyple/Non-Primitive/String
        System.out.println(isStudent+" "+c+" "+a+" "+b+" "+i+" "+l+" "+d+" "+f+" "+s);        
    }
}
