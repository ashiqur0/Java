/*
Java Methods : Parameters and Arguments
Parameters : received value by using parameters
Arguments : pass value to the calling function through arguments
*/
package Java_W3School._2_Java_Methods._2_Methods_Parameters;
public class _1_method_parameters {
    public static void main(String[] args) {
        sum(1, 2);
    }

    public static void sum(int a, int b) {
        System.out.println(a + b);
    }
}
