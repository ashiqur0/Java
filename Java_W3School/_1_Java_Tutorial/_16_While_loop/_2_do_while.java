/*
Java loop : do while loop
do while loop : perform the task once before checking the condition
*/ 


package Java_W3School._1_Java_Tutorial._16_While_loop;

public class _2_do_while {
    public static void main(String[] args) {
        int n = 1;
        do {
            System.out.print(n++ + " ");
        } while (n <= 5);
    }
}
