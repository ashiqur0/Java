/*
Java loop : while loop
while loop : perform repetitive task till the condition is true
*/


package Java_W3School._1_Java_Tutorial._16_While_loop;

public class _1_while {
    public static void main(String[] args) {
        int n = 1;
        while (n <= 5) {
            System.out.print(n++ + " ");
        }
    }
}
