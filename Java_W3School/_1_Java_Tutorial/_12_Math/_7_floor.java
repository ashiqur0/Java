/*
Java Math : Math.floor() method
Math.floor() : Round numbers down to the nearest integer
*/

public class _7_floor {
    public static void main(String[] args) {
        System.out.println(Math.floor(0.8));   // 0.0
        System.out.println(Math.floor(5.1));   // 5.0
        System.out.println(Math.floor(-6.3));   // -7.0
    }
}
