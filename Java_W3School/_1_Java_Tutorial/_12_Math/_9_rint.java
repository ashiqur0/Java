/*
Java Math : Math.rint() method
Math.rint() : round the number to the nearest integer of double type.
*/

public class _9_rint {
    public static void main(String[] args) {
        System.out.println(Math.rint(0.5)); // 0.0
        System.out.println(Math.rint(1.5)); // 2.0
        System.out.println(Math.rint(-5.1)); // -5.0
        System.out.println(Math.rint(-5.9)); // -6.0
    }
}
