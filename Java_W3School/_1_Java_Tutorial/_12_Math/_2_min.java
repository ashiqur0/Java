/*
Java Math : Math.min() method
Math.min() : returns the smaller of two values
*/

public class _2_min {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 8;
        int min_value = Math.min(num1, num2);
        System.out.println("Min Value : " + min_value);  // 5
    }
}
