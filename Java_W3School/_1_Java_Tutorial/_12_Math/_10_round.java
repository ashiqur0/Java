/*
Java Math : Math.round() method
Math.round() : round the number to the nearest integer.
*/

public class _10_round {
    public static void main(String[] args) {
        System.out.println(Math.round(0.5)); // 1
        System.out.println(Math.round(1.5)); // 2
        System.out.println(Math.round(-5.1)); // -5
        System.out.println(Math.round(-5.9)); // -6
    }
}
