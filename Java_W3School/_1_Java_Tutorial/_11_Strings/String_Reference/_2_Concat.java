/*
String Reference : concat() method
concat() : appends a string to end of another string 
*/

package Java_W3School._1_Java_Tutorial._11_Strings.String_Reference;

public class _2_concat {
    public static void main(String[] args) {
        String first = "Ashiqur ";
        String last =  "Rahman";
        System.out.println(first.concat(last)); // Ashiqur Rahman
    }
}
