package Java_W3School._1_Java_Tutorial._7_Variables;

public class Java_Variables {
    public static void main(String[] args) {
        // Declaring and Creating Variables
        String name = "Samiul";
        int roll = 1;
        float gpa = 3.88f;
        char grade = 'A';
        boolean isPassed = true;

        // Assign new value to existign variable 
        name = "Nawshin";
        roll = 2;
        gpa = 3.78f;
        grade = 'A';
        isPassed = false;

        // Final Variables : the value is constant (unchangable)
        final int marks = 98;
    }
}
