/*
Java : Characters Types
Character types : is used to store single character. 
*/

package Java_W3School._1_Java_Tutorial._8_Data_Types;

public class _4_Characters {
    public static void main(String[] args) {
        char grade = 'A';
        System.out.println(grade);

        // it can also store ASCII values
        char myChar = 65, myChar2 = 66, myChar3 = 90;
        System.out.println(myChar + " " + myChar2 + " " + myChar3);
    }
}
