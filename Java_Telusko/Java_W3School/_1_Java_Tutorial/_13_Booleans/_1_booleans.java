/*
Java Booleans : A data type that can holde true or false value
Booleans : Yes / No, True / False, On / Off
*/

package Java_Telusko.Java_W3School._1_Java_Tutorial._13_Booleans;
public class _1_booleans {
    public static void main(String[] args) {
        // Boolean value
        boolean isPrime = false;
        System.out.println(isPrime);    // false

        // Boolean Expression
        System.out.println(4 < 5);  // true

        // Real Life example
        int voteAge = 18;
        int age = 21;
        System.out.println(age >= voteAge); // true
    }
}
