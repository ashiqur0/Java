package Java_Telusko.Java_W3School._1_Java_Tutorial._11_Strings;

public class _3_Number_and_String {
    public static void main(String[] args) {

        // Addition of integer numbers
        int x = 10, y = 20;
        int z = x + y;
        System.out.println("Addition: " + z);

        // Concatinations of Strings
        String a = "10", b = "20";
        String c = a + b;
        System.out.println("Concatination: " + c);
    }
}
