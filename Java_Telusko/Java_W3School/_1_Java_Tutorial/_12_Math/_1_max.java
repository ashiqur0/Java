/*
Java Math : Math.max() method
Math.max() : returns the greater of two value
*/

public class _1_max {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 13;
        int max_value = Math.max(num1, num2);
        System.out.println("Max Value :" + max_value);  // 13
    }
}
