package Java_Telusko.Java_W3School._1_Java_Tutorial._5_Output;

public class Print_Text {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("I'm Learning Java");
        System.out.println("It's awesome");
        
        // Double quotes : Text must be wrapped inside the double quotation marks ""
        System.out.println("I'm Learning Java");    // execute successfully
        // System.out.println(I'm Learning Java);     // error

        // print() method : print without line brake
        System.out.print("Hello World!");
        System.out.print("I'll print on the same line.");
    }
}
