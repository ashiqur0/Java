/*
Java if-else : else if statement
else-if: check the conditin if the previous condition was false
*/

package Java_Telusko.Java_W3School._1_Java_Tutorial._14_If_Else;

public class _3_else_if {
    public static void main(String[] args) {
        int x = 4;
        int y = 8;
        if (x == y) {
            System.out.println("x is equal to y");
        } else if (x < y) {
            System.out.println("x is less than y");
        } else {
            System.out.println("x is greater than y");
        }
    }
}
