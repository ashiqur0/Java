/*
Java loop : for loop
for loop : perform repetitive task till the condition is true
*/
package Java_Telusko.Java_W3School._1_Java_Tutorial._17_For_Loop;

public class _1_for {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
    }
}
